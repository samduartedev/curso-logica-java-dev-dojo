package conditionals.conditionalsifelse;

public class OddOrEven {
    public static void main(String[] args) {
        int number = 25;
        if (number%2 == 0){
            System.out.println("The number " + number + " is even");
        } else {
            System.out.println("The number " + number + " is odd");
        }
    }
}
